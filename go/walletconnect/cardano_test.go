// --- In-Go round-trip (independent of the TS-minted KATs). ---

// cardanoBaseAddressInGo builds a mainnet base address (type 0): header ||
// paymentHash(28) || stakeHash(28), encoded with the inline bech32 ENCODER from
// cardano_test (sign side) so drift surfaces as a failing round-trip.
func cardanoBaseAddressInGo(t *testing.T, pub []byte) (raw []byte, bech string) {
	t.Helper()
	header := byte((0 << 4) | 1) // type 0 base, network 1 mainnet
	stakeHash := blake2b224([]byte("stake-" + hex.EncodeToString(pub)))
	raw = make([]byte, 0, 1+28+28)
	raw = append(raw, header)
	raw = append(raw, blake2b224(pub)...)
	raw = append(raw, stakeHash...)
	bech = cardanoBech32EncodeTest(t, "addr", raw)
	return raw, bech
}

func cardanoMessageInGo(t *testing.T, bech string) string {
	t.Helper()
	msg, err := BuildSiwxMessage(BuildParams{
		Challenge: LoginChallenge{
			Domain:   "hanzo.id",
			URI:      "https://hanzo.id/login",
			Nonce:    "0123456789abcdef0123456789abcdef",
			IssuedAt: "2026-01-01T00:00:00.000Z",
		},
		Address: bech,
		Chain:   ChainCardano,
	})
	if err != nil {
		t.Fatalf("BuildSiwxMessage: %v", err)
	}
	return msg
}

// cardanoBech32EncodeTest is a tiny bech32 ENCODER for the test (the verifier
// only decodes). Mirrors the TS bech32Encode.
func cardanoBech32EncodeTest(t *testing.T, hrp string, data []byte) string {
	t.Helper()
	// 8 -> 5 bit, pad=true
	var acc uint32
	var bits int
	data5 := make([]int, 0, len(data)*8/5+1)
	for _, b := range data {
		acc = ((acc << 8) | uint32(b)) & 0xffffffff
		bits += 8
		for bits >= 5 {
			bits -= 5
			data5 = append(data5, int((acc>>uint(bits))&31))
		}
	}
	if bits > 0 {
		data5 = append(data5, int((acc<<uint(5-bits))&31))
	}
	// checksum
	values := append(cardanoBech32HrpExpand(hrp), data5...)
	mod := cardanoBech32Polymod(append(values, 0, 0, 0, 0, 0, 0)) ^ cardanoBech32Const
	for i := 0; i < 6; i++ {
		data5 = append(data5, int((mod>>uint(5*(5-i)))&31))
	}
	out := hrp + "1"
	for _, d := range data5 {
		out += string(cardanoBech32Charset[d])
	}
	return out
}

// cardanoEncProtectedMapTest encodes { 1: -8, "address": raw } (sign side).
func cardanoEncProtectedMapTest(addr []byte) []byte {
	out := cborEncodeHead(5, 2) // map(2)
	out = append(out, cborEncodeHead(0, 1)...)
	out = append(out, byte(0x27)) // -8 (negint: 0x20|7)
	out = append(out, cborEncodeText("address")...)
	out = append(out, cborEncodeBytes(addr)...)
	return out
}

// cardanoEncCoseSign1Test encodes [protected, {hashed:false}, payload, sig].
func cardanoEncCoseSign1Test(protectedSer, payload, sig []byte) []byte {
	out := cborEncodeHead(4, 4)
	out = append(out, cborEncodeBytes(protectedSer)...)
	out = append(out, cborEncodeHead(5, 1)...)     // map(1)
	out = append(out, cborEncodeText("hashed")...) // "hashed"
	out = append(out, 0xf4)                        // false
	out = append(out, cborEncodeBytes(payload)...)
	out = append(out, cborEncodeBytes(sig)...)
	return out
}

func TestCardanoRoundTripInGo(t *testing.T) {
	pub, priv, err := ed25519.GenerateKey(rand.Reader)
	if err != nil {
		t.Fatalf("ed25519 keygen: %v", err)
	}
	addrRaw, bech := cardanoBaseAddressInGo(t, pub)
	msg := cardanoMessageInGo(t, bech)

	protectedSer := cardanoEncProtectedMapTest(addrRaw)
	payload := []byte(msg)
	sigStruct := cborBuildSigStructure(protectedSer, []byte{}, payload)
	sig := ed25519.Sign(priv, sigStruct)
	coseSign1 := cardanoEncCoseSign1Test(protectedSer, payload, sig)

	p := Proof{
		Chain:     ChainCardano,
		Scheme:    SchemeEd25519Cardano,
		Address:   bech,
		PublicKey: hex.EncodeToString(pub),
		Message:   msg,
		Signature: hex.EncodeToString(coseSign1),
	}
	if !VerifyCardano(p) {
		t.Fatal("VerifyCardano(in-Go round-trip) = false, want true")
	}
	p.Message += "x"
	if VerifyCardano(p) {
		t.Fatal("VerifyCardano(tampered in-Go) = true, want false")
	}
}

