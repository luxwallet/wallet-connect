func TestCardanoDoesNotPanicOnGarbage(t *testing.T) {
	garbage := Proof{
		Chain:     ChainCardano,
		Scheme:    SchemeEd25519Cardano,
		Address:   "!!!notbech32!!!",
		PublicKey: "nothex",
		Message:   "not a siwx message",
		Signature: "!!!!",
	}
	defer func() {
		if r := recover(); r != nil {
			t.Fatalf("VerifyCardano panicked on garbage: %v", r)
		}
	}()
	if VerifyCardano(garbage) {
		t.Fatal("VerifyCardano(garbage) = true, want false")
	}
}

