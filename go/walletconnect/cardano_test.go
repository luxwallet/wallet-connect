//
//  1. Cross-language KAT vectors: COSE_Sign1 + COSE_Key produced by the TS side
//     (@noble ed25519 + the TS CBOR encoder, the exact bytes a CIP-30 wallet
//     emits) over the EXACT CAIP-122 message, which Go MUST verify byte-for-byte.
//     If the Go verifier ever drifts from the TS verifier, these fail loudly.
//  2. In-Go round-trips (ed25519 stdlib, blake2b-224, the Go CBOR encoder) plus
//     fail-closed hardening.
//
// ed25519 is stdlib; blake2b is golang.org/x/crypto (BSD-3); CBOR/bech32 are
// inline. Zero copyleft.

