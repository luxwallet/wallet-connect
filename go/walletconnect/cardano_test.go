func (k cardanoKAT) proof() Proof {
	return Proof{
		Chain:     ChainCardano,
		Scheme:    SchemeEd25519Cardano,
		Address:   k.address,
		PublicKey: k.publicKey,
		Message:   k.message,
		Signature: k.signature,
		Extra:     map[string]any{"coseKey": k.coseKey},
	}
}

